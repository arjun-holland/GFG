class Solution {
  public:
    vector<vector<int>> kClosest(vector<vector<int>>& points, int k) {
        // Your code here
        vector<vector<int>> ans;
        vector<vector<int>> h;

        for(auto v : points){
            int x = v[0];
            int y = v[1];
            int d = abs((x*x)+(y*y));
            h.push_back({d,x,y});
        }
        sort(h.begin(),h.end());
        int i=0;
        while(k--){
            ans.push_back({h[i][1],h[i][2]});
            i++;
        }
        return ans;
    }
};

