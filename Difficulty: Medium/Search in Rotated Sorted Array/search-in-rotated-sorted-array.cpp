class Solution {
  public:
    int search(vector<int>& arr, int key) {
        int i=0,j=arr.size()-1;
        while(i<=j){
            if(arr[i] == key){
                return i;
            }else{
                i++;
            }
            if(arr[j] == key){
                return j;
            }else{
                j--;
            }
        }
        return -1;
    }
};

