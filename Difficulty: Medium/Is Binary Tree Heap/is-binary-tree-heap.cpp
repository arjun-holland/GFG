class Solution {
  public:
    bool isHeap(Node* tree) {
        queue<Node*> q;
        q.push(tree);
        
        while(!q.empty()){
           bool b = true;
           int sz = q.size();
           while(sz--){
            Node* r = q.front();
            int m = r->data;
            int c1=-1,c2=-1;
            q.pop();
            if(r->left){
                q.push(r->left);
                c1 = r->left->data;
            }
            if(r->right){
                q.push(r->right);
                c2 = r->right->data;
            }
            if(b == false && (r->left != NULL || r->right != NULL))return false;
            if(m < c1 || m < c2)return false;
            if(r->left != NULL && r->right == NULL){
                b = false;
                continue;
            }
            if(r->right != NULL && r->left == NULL)return false;
            if(c1 == -1 && c2 == -1)continue;
           }
        }
        return true;
    }
};


